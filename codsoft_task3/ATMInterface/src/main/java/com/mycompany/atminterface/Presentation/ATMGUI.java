    private void jToggleButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jToggleButton3MouseClicked
        // TODO add your handling code here:
        //Show Users
        int counter = 0;
        while(counter < 3) {
            String password = JOptionPane.showInputDialog(rootPane
                    , "Enter Password"
                    , "Admin Page"
                    , JOptionPane.QUESTION_MESSAGE);
            int pass = Integer.parseInt(password);
            if(pass != 1234) {
                JOptionPane.showMessageDialog(rootPane
                        ,"Wrong Password, Try Again"
                        , "Error"
                        , JOptionPane.ERROR_MESSAGE);
            }
            if(pass == 1234) {
                try {
                    String name = JOptionPane.showInputDialog(rootPane,"Enter User Name To Show His Log ");
                    File userLog = new File(name + ".txt");
                    if(userLog.exists()){ 
                        JOptionPane.showMessageDialog(rootPane
                            ,ATMStart.getATMMachine().getFileService().readFromFile(name)
                            , "User Log"
                            , JOptionPane.INFORMATION_MESSAGE);
                    
                        break;
                    }else{
                        JOptionPane.showMessageDialog(rootPane
                                ,"The User Does Not Exist!"
                                , "About"
                                , JOptionPane.INFORMATION_MESSAGE);
                        break;
                    }
                } catch (IOException ex) {
                    Logger.getLogger(ATMGUI.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            counter++;
        }
        if(counter == 3)
            JOptionPane.showMessageDialog(rootPane
                        ,"Sorry, You Have Exceeded The Limit Of Trials"
                        , "Error"
                        , JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_jToggleButton3MouseClicked

